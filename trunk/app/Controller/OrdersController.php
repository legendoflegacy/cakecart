    public function checkout(){

        if ($this->request->is('post')) {
            $this->Order->create();
            $this->request->data['Order']['user_id'] = $this->Auth->user('id');

            if ($this->Order->save($this->request->data)) {
                $this->Session->setFlash(__('The order has been saved'));


                $cart = $this->requestAction(array('controller'=>'Carts','action'=>'getCart'),array('return'));
                //debug($cart);
                $orderDetail = array();
                foreach($cart as $cartItem){

                    $perOrder = Array(
                        'Orderdetail'=> Array(
                            'order_id'=>$this->Order->id,
                            'product_id'=>$cartItem['Cart']['product_id']
                        )
                    );
                    array_push($orderDetail, $perOrder);
                }
                $this->requestAction(array('controller'=>'Orderdetails','action'=>'addDetail'),array('return','orderDetail' => $orderDetail));
                $this->requestAction(array('controller'=>'Carts','action'=>'emptyCart'));
                //debug($orderDetail);
                $this->redirect(array('action' => 'index','admin'=>false));
            } else {
                $this->Session->setFlash(__('The order could not be saved. Please, try again.'));
            }
        }
        //debug($this->Auth->user());
        $this->render('add');
    }
